    private void idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idActionPerformed
        String DrugID=id.getText();
         try
        {   
            
           
            
                 Connection con= Connectionprovider.getcon();
                 Statement st=con.createStatement();
                 ResultSet rs1=st.executeQuery("select *from store where DrugId ='"+DrugID+"'");
                 if (rs1.next()){
                    name.setText(rs1.getString(2));
                    sname.setText(rs1.getString(3));
                    cname.setText(rs1.getString(4));
                    inStockM.setText(rs1.getString(5));
                    description.setText(rs1.getString(6));
                    idate.setText(rs1.getString(7));
                    edate.setText(rs1.getString(8));
                    price.setText(rs1.getString(9));
                    row.setText(rs1.getString(10));
                    column.setText(rs1.getString(11));
                    inStockA.setText("0");
                    inStockB.setText("0");
                  
                 }   
                 
              
                 Statement st2=con.createStatement();
                ResultSet rs2=st2.executeQuery("select *from brancha where DrugId ='"+DrugID+"'");
                if(rs2.next())
                 {
                      inStockA.setText(rs2.getString(5));
                    
                 }
                 
              
                Statement st3=con.createStatement();
                 ResultSet rs3=st3.executeQuery("select *from branchb where DrugId ='"+DrugID+"'");
                if(rs3.next())
                {
                      inStockB.setText(rs3.getString(5));
                   
                }
                 
      
            
            }
         catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
    }//GEN-LAST:event_idActionPerformed

