    private void ShiftButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShiftButtonActionPerformed
        String Id = id.getText();
        String Name = name.getText();
        String Cname = cname.getText();
        String Sname = sname.getText();
        String Idate = idate.getText();
        String Edate = edate.getText();
        String Price = price.getText();
        String Des = description.getText();
        String Row = row.getText();
        String Column = column.getText();
        String InM = inStockM.getText();
        String InA = inStockA.getText();
        String InB = inStockB.getText();
        String Quantity = quantity.getText();
        int shiftM = Integer.parseInt(InM) - Integer.parseInt(Quantity);
        int shiftA = Integer.parseInt(InA) + Integer.parseInt(Quantity);
        int shiftB = Integer.parseInt(InB) + Integer.parseInt(Quantity);
        String mainS = String.valueOf(shiftM);
        String branchA = String.valueOf(shiftA);
        String branchB = String.valueOf(shiftB);
        try{
             int selected =  jComboBox1.getSelectedIndex();
             Connection con= Connectionprovider.getcon();
             Statement st=con.createStatement();
             st.executeUpdate("delete from store where DrugId ='"+Id+"'");
             st.executeUpdate("Insert into store value('"+Id+"','"+Name+"','"+Cname+"','"+Sname+"','"+mainS+"','"+Des+"','"+Idate+"','"+Edate+"','"+Price+"','"+Row+"','"+Column+"')"); 
             
             if(selected == 0)
             {
                 Statement st2=con.createStatement();
                 ResultSet rs2=st2.executeQuery("select *from brancha where DrugId ='"+Id+"'");
                 if (rs2.next()){
                     st.executeUpdate("delete from brancha where DrugId ='"+Id+"'");
                 }
                 st.executeUpdate("Insert into brancha value('"+Id+"','"+Name+"','"+Cname+"','"+Sname+"','"+branchA+"','"+Des+"','"+Idate+"','"+Edate+"','"+Price+"','"+Row+"','"+Column+"')"); 
                 
                 JOptionPane.showMessageDialog(null,"Successfully Shifted To Branch A");
                 setVisible(false);
                 new ShiftProduct().setVisible(true);
             }
        
             if(selected == 1)
             {
                 Statement st3=con.createStatement();
                 ResultSet rs3=st3.executeQuery("select *from branchb where DrugId ='"+Id+"'");
                 if (rs3.next()){
                     st.executeUpdate("delete from branchb where DrugId ='"+Id+"'");
                 }
                 st.executeUpdate("Insert into branchb value('"+Id+"','"+Name+"','"+Cname+"','"+Sname+"','"+branchB+"','"+Des+"','"+Idate+"','"+Edate+"','"+Price+"','"+Row+"','"+Column+"')"); 
             
                 JOptionPane.showMessageDialog(null,"Successfully Shifted To Branch B");
                 setVisible(false);
                 new ShiftProduct().setVisible(true);
             }
             
             
        }catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
       
    }//GEN-LAST:event_ShiftButtonActionPerformed

